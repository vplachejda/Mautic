namespace MauticPlugin\IntegrationsBundle\Exception;


class RequiredFieldsMissingException extends \Exception
{
    /**
     * @var array
     */
    private $fields;

    public function __construct(array $fields)
    {
        $this->fields = $fields;
    }

    /**
     * @return array
     */
    public function getFields(): array
    {
        return $this->fields;
    }
}